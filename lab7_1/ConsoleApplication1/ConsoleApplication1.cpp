{
    SetConsoleOutputCP(1251);
    using namespace std;
    int a, b;
    cout << "Введи число а:" << endl;
    cin >> a;
    cout << "Введи число b:" << endl;
    cin >> b;
    cout <<"Сума чисел:"<< sum(a, b) << endl;

    int n;
    cout << "Введіть число для перевірки, чи просте воно(якщо 1-число просте, 0-складне):" << endl;
    cin >> n;
    cout << isPrime(n) << endl;

    int x;
    cout << "Введи число для обчислення його факторіалу:" << endl;
    cin >> x;
    cout << factorial(x) << endl;
    
}

