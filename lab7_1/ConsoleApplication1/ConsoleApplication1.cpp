#include <iostream>
#include <windows.h>
int sum(int a, int b)
{
    return a + b;
}

bool isPrime(int n)
{
    bool prime = true; 
    for (int i = 2; i < n; i++)
    {
        if (n % i == 0)
        {
            prime = false;
        }
    }
    return prime;
}

int factorial(int x)
{
    int res = 1;
    for (int i = 1; i <=x; i++)
    {
        res = res * i;
    }
    return res;
}


